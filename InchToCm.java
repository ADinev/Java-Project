package swiftAcademy.Lecture2;

public class InchToCm {

    public static void main(String[] args) {

        {
            float inch = 2.54f;

            inch *= 12;

            System.out.println("12 Incha = " + inch);
            
        }
        
        {
            float inch = 2.54f;
            
            inch *= 23.23;
            
            System.out.println("23.23 Incha = " + inch);
            
        }
    }
}
